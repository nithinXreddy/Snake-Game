import turtle

# Create a turtle screen
screen = turtle.Screen()

# Draw something...
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)

# Manually update the screen
screen.update()

turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
# Keep the window open until it's closed by the user
screen.mainloop()
